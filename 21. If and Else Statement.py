# if inline
nama = input('\nmasukkan namamu: ')
if nama == 'ucup': print('Kamu ganteng bingitzz')
print(f'Terima kasih {nama}')

# if indentation
nama = input('\nmasukkan namamu: ')
if nama == 'ucup':
    print('Kamu ganteng bingitzz')
    print('Kamu juga ketjehh')
print(f'Terima kasih {nama}')

# else statement
nama = input('\nmasukkan namamu: ')
if nama == 'otong':
    print('Hai otooong, kamu kerenn')
else:
    print('Ah kamu bukan otong, kamu gak keren :(')

print(f'Byee {nama}')